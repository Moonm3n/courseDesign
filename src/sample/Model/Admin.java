package sample.Model;

public class Admin {

    private String administorId;
    private String password;
    private String administorName;

    public Admin() {
    }

    public String getAdministorId() {
        return administorId;
    }

    public void setAdministorId(String administorId) {
        this.administorId = administorId;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getAdministorName() {
        return administorName;
    }

    public void setAdministorName(String administorName) {
        this.administorName = administorName;
    }
}
